package js.webtools;

/**
 * A common interface that our HttpServlets and js.webTesting mock servlets will
 * implement to facilitate unit tests
 */
public interface OurServletInterface {

  /**
   * User can override this to perform initialization. Default implementation
   * does nothing
   */
  void ourInit();

  void serviceRequest(WebResponse webResponse) throws Exception;

  String getNameForTests();
}
